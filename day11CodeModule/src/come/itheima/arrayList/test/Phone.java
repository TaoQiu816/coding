package come.itheima.arrayList.test;

public class Phone {
    private String band;
    private int price;

    public Phone() {
    }

    public Phone(String band, int price) {
        this.band = band;
        this.price = price;
    }

    public String getBand() {
        return band;
    }

    public void setBand(String band) {
        this.band = band;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}


