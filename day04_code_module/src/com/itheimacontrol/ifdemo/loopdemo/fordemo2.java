package com.itheimacontrol.ifdemo.loopdemo;

public class fordemo2 {
    public static void main(String[] args) {
        //顺序打印1——5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        //倒序打印5-1
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
