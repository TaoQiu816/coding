package com.itheimacontrol.ifdemo.loopdemo;

public class whiledemo1 {
    public static void main(String[] args) {
        //利用while循环打印1到100
        int i =0;//初始化语句
        while (i<=100){//条件判断语句
            System.out.println(i);//循环体
            i++;//条件控制
        }
    }
}
//for循环通常用于知道循环次数，或者循环范围的情况
//while循环通常用于不知道循环的次数和范围，只知道循环的结束条件的情况
